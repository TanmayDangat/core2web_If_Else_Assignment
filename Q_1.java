package core2web.Control_Statements.If_Else_Assignment;

public class Q_1 {
    public static void main(String[] args) {
        int x = 200;
        if((x >= 1) && (x <= 1000)){
            System.out.println(x + " is in the range 1 to 1000");
        }
        else{
            System.out.println(x + " is not in the range 1 to 1000");
        }
    }
}
